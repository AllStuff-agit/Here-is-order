#!/usr/bin/env node

import { pbkdf2Sync, randomBytes, randomUUID } from 'node:crypto';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

export const PASSWORD_HASH_SCHEME = 'pbkdf2_sha256';
export const PASSWORD_HASH_ITERATIONS = 100_000;
export const PASSWORD_SALT_BYTES = 16;
export const PASSWORD_HASH_BYTES = 32;

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
export const DEFAULT_OUTPUT_PATH = path.resolve(SCRIPT_DIR, '..', 'data', 'seed_admin.sql');

function characterCount(value) {
  return Array.from(value).length;
}

function requireSqlText(value, variableName) {
  if (!value) {
    throw new Error(`${variableName} 환경변수는 비워둘 수 없습니다.`);
  }
  if (value.includes('\0')) {
    throw new Error(`${variableName} 환경변수에는 NUL 문자를 사용할 수 없습니다.`);
  }
  return value;
}

export function readAdminConfig(env = process.env) {
  if (!env.ADMIN_PASSWORD) {
    throw new Error('ADMIN_PASSWORD 환경변수를 설정해야 합니다.');
  }
  if (characterCount(env.ADMIN_PASSWORD) < 12) {
    throw new Error('ADMIN_PASSWORD는 12자 이상이어야 합니다.');
  }

  const username = requireSqlText(
    env.ADMIN_USERNAME === undefined ? 'admin' : env.ADMIN_USERNAME.trim(),
    'ADMIN_USERNAME',
  );
  const name = requireSqlText(
    env.ADMIN_NAME === undefined ? '관리자' : env.ADMIN_NAME.trim(),
    'ADMIN_NAME',
  );

  return {
    username,
    password: env.ADMIN_PASSWORD,
    name,
  };
}

export function createPasswordHash(password, salt = randomBytes(PASSWORD_SALT_BYTES)) {
  const saltBuffer = Buffer.from(salt);
  if (saltBuffer.length !== PASSWORD_SALT_BYTES) {
    throw new Error(`salt는 ${PASSWORD_SALT_BYTES}바이트여야 합니다.`);
  }

  const hash = pbkdf2Sync(
    password,
    saltBuffer,
    PASSWORD_HASH_ITERATIONS,
    PASSWORD_HASH_BYTES,
    'sha256',
  );

  return [
    PASSWORD_HASH_SCHEME,
    PASSWORD_HASH_ITERATIONS,
    saltBuffer.toString('hex'),
    hash.toString('hex'),
  ].join('$');
}

export function sqlText(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

export function buildAdminSeedSql({ username, passwordHash, name }) {
  return [
    '-- Generated by scripts/generate-admin-seed.mjs. Do not commit this file.',
    'INSERT INTO users (username, password_hash, name, role, is_active, is_deleted, deleted_at)',
    `VALUES (${sqlText(username)}, ${sqlText(passwordHash)}, ${sqlText(name)}, 'admin', 1, 0, NULL)`,
    'ON CONFLICT(username) DO UPDATE SET',
    '  password_hash = excluded.password_hash,',
    '  name = excluded.name,',
    "  role = 'admin',",
    '  is_active = 1,',
    '  is_deleted = 0,',
    '  deleted_at = NULL,',
    "  updated_at = datetime('now');",
    '',
  ].join('\n');
}

function writePrivateFileAtomically(outputPath, contents) {
  const outputDir = path.dirname(outputPath);
  fs.mkdirSync(outputDir, { recursive: true, mode: 0o700 });

  const temporaryPath = path.join(outputDir, `.${path.basename(outputPath)}.${randomUUID()}.tmp`);
  try {
    fs.writeFileSync(temporaryPath, contents, {
      encoding: 'utf8',
      flag: 'wx',
      mode: 0o600,
    });
    fs.renameSync(temporaryPath, outputPath);
    fs.chmodSync(outputPath, 0o600);
  } catch (error) {
    try {
      fs.unlinkSync(temporaryPath);
    } catch {
      // 생성 전 실패했거나 이미 정리된 임시 파일입니다.
    }
    throw error;
  }
}

export function generateAdminSeed({
  env = process.env,
  outputPath = DEFAULT_OUTPUT_PATH,
  salt,
  log = console.log,
} = {}) {
  const config = readAdminConfig(env);
  const passwordHash = createPasswordHash(config.password, salt);
  const sql = buildAdminSeedSql({
    username: config.username,
    passwordHash,
    name: config.name,
  });

  writePrivateFileAtomically(outputPath, sql);
  log(`관리자 seed SQL을 생성했습니다: ${path.relative(process.cwd(), outputPath) || outputPath}`);
  return outputPath;
}

export function runCli() {
  try {
    generateAdminSeed();
  } catch (error) {
    const message = error instanceof Error ? error.message : '알 수 없는 오류';
    console.error(`관리자 seed 생성 실패: ${message}`);
    process.exitCode = 1;
  }
}

const isMain = process.argv[1]
  && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isMain) {
  runCli();
}
